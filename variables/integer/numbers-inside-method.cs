void WorkWithIntegers()
{
    int a = 18;
    int b = 6;

    // Addition
    int sum = a + b; // 24
    Console.WriteLine($"The sum of a and b is: {sum}\n");

    // Subtraction
    int difference = a - b; // 12
    Console.WriteLine($"The difference between a and b is: {difference}\n");

    // Multiplication
    int product = a * b; // 108
    Console.WriteLine($"The product of a and b is: {product}\n");

    // Division
    int quotient = a / b; // 3
    Console.WriteLine($"The quotient of a divided by b is: {quotient}\n");
    // Modulus
    float remainder = a % b; // 0
    Console.WriteLine($"The remainder of a divided by b is: {remainder}\n");
}